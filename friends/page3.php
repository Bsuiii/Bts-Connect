<p class="no"><u>C’est quoi ?</u></p>
<p><b>BTS au Maroc est un diplôme qui a été créé en <i>1992</i> par le Ministère de l’Éducation Nationale dans l’objectif d’améliorer l’adéquation formation-emploi et de répondre aux besoins des entreprises en ressources humaines opérationnelles dans les secteurs de l’industrie, du commerce et des services. Ce diplôme connaît un franc succès aussi bien auprès des jeunes bacheliers qu’auprès des employeurs. La formation prépare des techniciens supérieurs et des cadres moyens dans différentes spécialités, dotés de compétences professionnelles avec une dimension scientifique qui leur permet d’évoluer et de s’adapter aux changements. L'effectif des étudiants inscrits en classes de préparation du BTS a atteint <i>4200</i> dans les centres publics et <i>232</i> dans les centres privés au titre de l’année scolaire <i>2013-2014</i>.</b></p>

